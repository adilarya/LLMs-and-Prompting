"""Evaluation utilities: extract answers from model output and score them."""

import re
import json
import os
from datetime import datetime
from typing import List, Dict, Any


# ---------------------------------------------------------------------------
# Answer extraction
# ---------------------------------------------------------------------------

def extract_answer(raw_output: str, prompt_type: str = "zero_shot") -> str:
    """Extract the model's answer from its raw generated text.

    Strategy:
    - For CoT prompts the model is instructed to produce a line starting with
      'Answer:'.  We search for that marker first.
    - As a fallback we return the first non-empty line of the output stripped
      of leading/trailing whitespace.

    Args:
        raw_output: The full text generated by the model (may include the
                    prompt prefix if the tokenizer does not strip it).
        prompt_type: One of 'zero_shot', 'few_shot', or 'cot'.

    Returns:
        A cleaned answer string.
    """
    # Try to find an explicit "Answer:" marker (used in few-shot and CoT prompts)
    answer_match = re.search(r"Answer\s*:\s*(.+)", raw_output, re.IGNORECASE)
    if answer_match:
        return answer_match.group(1).strip().split("\n")[0].strip()

    # Fallback: return the first non-empty line
    for line in raw_output.splitlines():
        stripped = line.strip()
        if stripped:
            return stripped

    return raw_output.strip()


# ---------------------------------------------------------------------------
# Scoring
# ---------------------------------------------------------------------------

def score_answer(predicted: str, expected: str) -> int:
    """Return 1 if the predicted answer contains the expected answer, else 0.

    Comparison is case-insensitive and ignores leading/trailing whitespace.

    Args:
        predicted: The answer extracted from the model output.
        expected: The ground-truth answer from the dataset.

    Returns:
        1 for correct, 0 for incorrect.
    """
    predicted_norm = predicted.lower().strip()
    expected_norm = expected.lower().strip()
    return int(expected_norm in predicted_norm)


def compute_summary(results: List[Dict[str, Any]]) -> Dict[str, Any]:
    """Compute aggregate accuracy statistics from a list of scored results.

    Args:
        results: List of result dicts, each containing a 'score' key (0 or 1).

    Returns:
        Dict with keys: total, correct, accuracy.
    """
    total = len(results)
    correct = sum(r["score"] for r in results)
    accuracy = round(correct / total, 4) if total > 0 else 0.0
    return {"total": total, "correct": correct, "accuracy": accuracy}


# ---------------------------------------------------------------------------
# JSON output writer
# ---------------------------------------------------------------------------

def save_results(
    results: List[Dict[str, Any]],
    experiment: str,
    model_name: str,
    output_dir: str = None,
) -> str:
    """Save experiment results to a JSON file.

    The output file is written to *output_dir* (defaults to the project-level
    ``results/`` directory) and named ``<experiment>_<model_slug>.json``.

    Output schema::

        {
          "experiment": "task1_zero_shot",
          "model": "EleutherAI/gpt-neo-1.3B",
          "timestamp": "2026-04-02T20:21:34",
          "results": [
            {
              "id": 0,
              "question": "...",
              "expected": "...",
              "prompt": "...",
              "output": "...",
              "predicted": "...",
              "score": 1
            }
          ],
          "summary": {
            "total": 30,
            "correct": 15,
            "accuracy": 0.5
          }
        }

    Args:
        results: List of per-example result dicts.
        experiment: Experiment name (e.g. 'task1_zero_shot').
        model_name: HuggingFace model identifier.
        output_dir: Directory to write the JSON file.  Created if it does not
                    exist.

    Returns:
        Absolute path to the written JSON file.
    """
    if output_dir is None:
        base_dir = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
        output_dir = os.path.join(base_dir, "results")

    os.makedirs(output_dir, exist_ok=True)

    model_slug = model_name.replace("/", "_").replace("-", "_")
    filename = f"{experiment}_{model_slug}.json"
    filepath = os.path.join(output_dir, filename)

    payload = {
        "experiment": experiment,
        "model": model_name,
        "timestamp": datetime.utcnow().strftime("%Y-%m-%dT%H:%M:%S"),
        "results": results,
        "summary": compute_summary(results),
    }

    with open(filepath, "w", encoding="utf-8") as f:
        json.dump(payload, f, indent=2, ensure_ascii=False)

    return filepath
