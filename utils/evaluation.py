"""Evaluation utilities: extract answers from model output and score them."""

import re
import json
import os
from datetime import datetime
from typing import Any, Dict, List


# ---------------------------------------------------------------------------
# Answer extraction
# ---------------------------------------------------------------------------

def extract_answer(raw_output: str, prompt_type: str = "zero_shot") -> str:
    """Extract the model's answer from its raw generated text.

    Strategy:
    - For CoT prompts the model may produce a line starting with 'Answer:'.
    - Otherwise return the first non-empty line.

    Args:
        raw_output: The full text generated by the model.
        prompt_type: One of 'zero_shot', 'few_shot', or 'cot'.

    Returns:
        A cleaned answer string.
    """
    answer_match = re.search(r"Answer\s*:\s*(.+)", raw_output, re.IGNORECASE)
    if answer_match:
        return answer_match.group(1).strip().split("\n")[0].strip()

    for line in raw_output.splitlines():
        stripped = line.strip()
        if stripped:
            return stripped

    return raw_output.strip()


# ---------------------------------------------------------------------------
# Generic scoring helpers
# ---------------------------------------------------------------------------

def score_answer(predicted: str, expected: str) -> int:
    """Return 1 if predicted exactly matches expected after normalization."""
    predicted_norm = predicted.lower().strip()
    expected_norm = expected.lower().strip()
    return int(predicted_norm == expected_norm)


def check_format_valid(raw_output: str, prompt_type: str = "zero_shot") -> int:
    """Return 1 if the output appears to follow the expected response format."""
    if not raw_output.strip():
        return 0
    if prompt_type in ("few_shot", "cot"):
        return int(bool(re.search(r"Answer\s*:", raw_output, re.IGNORECASE)))
    return 1


def compute_summary(results: List[Dict[str, Any]]) -> Dict[str, Any]:
    """Compute aggregate accuracy statistics.

    Accepts either:
    - results with a top-level 'correct' key, or
    - results with a top-level 'score' key, or
    - Task 3-style results with scores['exact_match'].
    """
    total = len(results)
    correct = 0

    for r in results:
        if "correct" in r:
            correct += int(r["correct"])
        elif "score" in r:
            correct += int(r["score"])
        elif "scores" in r and "exact_match" in r["scores"]:
            correct += int(r["scores"]["exact_match"])

    accuracy = round(correct / total, 4) if total > 0 else 0.0
    return {"total": total, "correct": correct, "accuracy": accuracy}


# ---------------------------------------------------------------------------
# JSON output writers
# ---------------------------------------------------------------------------

def save_results(
    results: List[Dict[str, Any]],
    experiment: str,
    model_name: str,
    output_dir: str = None,
) -> str:
    """Save experiment results to a JSON file (internal schema)."""
    if output_dir is None:
        base_dir = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
        output_dir = os.path.join(base_dir, "results")

    os.makedirs(output_dir, exist_ok=True)

    model_slug = model_name.replace("/", "_").replace("-", "_")
    filename = f"{experiment}_{model_slug}.json"
    filepath = os.path.join(output_dir, filename)

    payload = {
        "experiment": experiment,
        "model": model_name,
        "timestamp": datetime.utcnow().strftime("%Y-%m-%dT%H:%M:%S"),
        "results": results,
        "summary": compute_summary(results),
    }

    with open(filepath, "w", encoding="utf-8") as f:
        json.dump(payload, f, indent=2, ensure_ascii=False)

    return filepath


def save_task3_json(
    records: List[Dict[str, Any]],
    filename: str,
    output_dir: str = None,
) -> str:
    """Save Task 3 results in the assignment-required JSON schema."""
    if output_dir is None:
        base_dir = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
        output_dir = os.path.join(base_dir, "results")

    os.makedirs(output_dir, exist_ok=True)
    filepath = os.path.join(output_dir, filename)

    with open(filepath, "w", encoding="utf-8") as f:
        json.dump(records, f, indent=2, ensure_ascii=False)

    return filepath